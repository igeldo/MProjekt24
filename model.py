class Person:
    def __init__(self, name, age, sex, fitness_level):
        self.name
        self.age = age
        self.sex = sex
        self.fitness_level = fitness_level

    def get_properties(self):
        return {
        'Name': self.name,
        'Age': self.age,
        'Sex': self.sex,
        'Fitness Level': self.fitness_level
        }
